#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const demoSiteDir = path.resolve(scriptDir, "..");
const sharedCssPath = path.resolve(demoSiteDir, "../shared/src/styles/base.css");
const gmailCssPath = path.resolve(demoSiteDir, "../gmail/src/gmail.css");
const outPath = path.resolve(demoSiteDir, "public/vendor/gmail-shadow.css");

function rewriteForShadowDom(css) {
  return css
    .replace(
      /(^|[\r\n])\s*html\s*,\s*body\s*,\s*#root\s*\{/gm,
      "$1:host {",
    )
    .replace(/(^|[\r\n])\s*html\s*,\s*body\s*\{/gm, "$1:host {")
    .replace(/(^|[\r\n])\s*body\s*,\s*html\s*\{/gm, "$1:host {")
    .replace(/(^|[\r\n])\s*html\s*\{/gm, "$1:host {")
    .replace(/(^|[\r\n])\s*body\s*\{/gm, "$1:host {")
    .replace(/:root/g, ":host");
}

async function main() {
  const [sharedCss, gmailCss] = await Promise.all([
    readFile(sharedCssPath, "utf8"),
    readFile(gmailCssPath, "utf8"),
  ]);

  const output = [
    "/* Generated by scripts/sync-gmail-shadow-css.mjs. */",
    ":host {",
    "  display: block;",
    "  min-height: 100%;",
    "}",
    ".gmail-shadow-root {",
    "  min-height: 100%;",
    "  height: 100%;",
    "}",
    "[data-wab-replay-highlight='true'] {",
    "  outline: 3px solid #1a73e8 !important;",
    "  outline-offset: 3px !important;",
    "  box-shadow: 0 0 0 6px rgba(26, 115, 232, 0.18) !important;",
    "  transition: outline-color 120ms ease, box-shadow 120ms ease;",
    "}",
    rewriteForShadowDom(sharedCss),
    rewriteForShadowDom(gmailCss),
    "",
  ].join("\n");

  await mkdir(path.dirname(outPath), { recursive: true });
  await writeFile(outPath, output, "utf8");
  console.log(`Wrote ${path.relative(demoSiteDir, outPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
